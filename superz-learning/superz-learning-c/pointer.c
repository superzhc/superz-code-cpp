/**
* 指针
 *
 * 数据在内存中的地址也称为指针，如果一个变量存储了一份数据的指针，就称它为指针变量。
 *
 * 在C语言中，允许用一个变量来存放指针，这种变量称为指针变量。指针变量的值就是某份数据的地址，这样的一份数据可以是数组、字符串、函数，也可以是另外的一个普通变量或指针变量。
*/

/**
 * 定义指针变量
 *
 * 定义指针变量与定义普通变量非常类似，不过要在变量名前面加星号*，格式为：
 * datatype *name;
 * 或者
 * datatype *name = value;
 *
 * 定义指针变量时必须带*，给指针变量赋值时不能带*。
 */
